        String date = dateformat.format(jDateChooser1.getDate());

        int EId = Integer.parseInt(jTextField1.getText());
        jRadioButton3.setActionCommand("yes");
        String status = jRadioButton3.getActionCommand();
        int nocount = Integer.parseInt(jTextField5.getText());
        // jDateChooser1.setCalendar(null);

        if (status.equals("yes")) {
            //UPDATE medical SET count(nocount);
            String sql1 = "INSERT INTO medical (EId,nocount,date) values ('" + EId + "','" + nocount + "','" + date + "')";

            try {

                pst = con.prepareStatement(sql1);
                pst.execute();

            } catch (Exception e) {
                System.out.println(e);
            }

        }


